    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        ClienteController controller = new ClienteController();
        ClienteModelo cm = new ClienteModelo();
        
        if(!ValidarCampos()){
            return;
        }
        cm.setNombre(txtNombre.getText());
        cm.setApellido(txtApellido.getText());
        cm.setTelefono(txtTelefono.getText());
        cm.setEmail(txtEmail.getText());
        cm.setDireccion(txtDireccion.getText());
        
        if(controller.insertar(cm)){
            JOptionPane.showMessageDialog(this, "Se Registro correctamente el cliente.");
            CargarTabla();
            LimpiarCampos();
        }else{
            JOptionPane.showMessageDialog(this, "No es pudo registrar el cliente.");
        }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
     ClienteModelo cm = new ClienteModelo();
     ClienteController controller = new ClienteController();
     
    if(txtIdCliente.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla");
    return;
    }   

    if(!ValidarCampos()){
        return;
    }
        cm.setIdCliente(idClienteSeleccionado);
        cm.setNombre(txtNombre.getText());
        cm.setApellido(txtApellido.getText());
        cm.setTelefono(txtTelefono.getText());
        cm.setEmail(txtEmail.getText());
        cm.setDireccion(txtDireccion.getText());
       
        
        if(controller.modificar(cm)){
            JOptionPane.showMessageDialog(this, "El cliente se modifico correctamente");
            CargarTabla();
            LimpiarCampos();
    }else{
            JOptionPane.showMessageDialog(this, "El cliente no se pudo modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed


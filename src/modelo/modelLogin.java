package modelo;

/**
 *
 * @author tequi
 */
public class modelLogin {
    int id;
    String username;
    String password;
    String name; 
    String lastname;
    String email;
    String telefono;
    String rol;
    boolean estado;
    
    
    public modelLogin(){
        id = 0;
        username = "";
        password = "";
        name = "";
        lastname = "";
        email = "";
        telefono = "";
        rol = "";
        estado = true;
    }

    public modelLogin(int id, String username, String password, String name, String lastname, String email, String telefono, String rol, boolean estado) {
        this.id = id;
        this.username = username;
        this.password = password;
        this.name = name;
        this.lastname = lastname;
        this.email = email;
        this.telefono = telefono;
        this.rol = rol;
        this.estado = estado;
    }
    
    
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public boolean isEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }
    
    
}
